            <p> Negara yang bernama lengkap Persatuan Negara-negara Meksiko atau United Mexican States ini memiliki luas wilayah sebesar 1.964.375 km2 dengan jumlah penduduk sebanyak 123.166.749 jiwa. Meksiko menduduki urutan ke-11 sebagai Negara yang memiliki Penduduk Terbanyak di Dunia. Mayoritas penduduk Meksiko beragama Katolik Roma yaitu sebanyak 82,7% dari seluruh rakyat Meksiko. Bahasa Spanyol yang merupakan bahasa Nasional ini digunakan oleh mayoritas penduduk Meksiko. Secara Astronomis, Meksiko berada diantara  14° – 33° LU dan 86° – 117° BT . Meksiko berbatasan dengan Amerika Serikat di sebelah Utara dan berbatasan dengan Belize dan Guatemala. Disebelah Barat Meksiko adalah Samudera Pasifik sedangkan disebelah Timur adalah Teluk Meksiko dan Laut Karibia. </p>
