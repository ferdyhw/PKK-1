            <h3> Politik</h3> 
            <p> Meksiko adalah sebuah negara yang terletak di Benua Amerika Utara dengan sistem pemerintahan Republik Federal Presidensil. Sistem Pemerintahan ini adalah sistem pemerintahan yang terdiri dari persekutuan beberapa negara bagian dengan kepala negara dan kepala pemerintahannya adalah seorang Presiden yang dipilih melalui pemilihan umum langsung setiap 6 tahun sekali. Ibukota Meksiko adalah Mexico City. </p>
            <p>Konstitusi 1917 memperuntukkan sebuah pemerintahan Persekutuan Republik dengan kekuasaan yang dibagikan kepada tiga institusi yang bebas yaitu eksekutif, legislatif, dan kehakiman. Dari segi sejarah, eksekutif sangat mendominasi kekuasaan lewat jabatan presiden. Sedang, kekuasaan hukum dalam Kongres tinggal mengikuti arahannya saja. Kongres terlihat aktif kembali sejak 1997 ketika partai oposisi tampil pertama dalam mayoritas legislatif. Namun, sejak partai oposisi mengambil alih kekuasaan pada 1997, Kongres semakin bebas dalam menggubal undang-undang. Presiden juga mempunyai kuasa di bawah perintah eksekutif untuk merancang undang-undang dalam bidang ekonomi, dan keuangan yang tertentu. Presiden dipilih setiap enam tahun, dan dia dilarang memegang jabatan yang sama untuk penggal kedua. Di Meksiko, tiada jabatan wakil presiden. Sekiranya seseorang Presiden itu dilucutkan jabatan atau meninggal dunia, Kongres akan melantik seorang Presiden Sementara. </p>
