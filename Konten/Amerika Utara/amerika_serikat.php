            <p>Amerika Serikat adalah federasi tertua di dunia yang masih tetap bertahan sampai saat ini. AS merupakan sebuah negara republik konstitusional dan demokrasi perwakilan, "dengan kekuasaan mayoritas dibatasi oleh hak-hak minoritas yang dilindungi oleh undang-undang".Pemerintahan diatur menurut sistem pemisahan kekuasaan yang ditetapkan oleh Konstitusi Amerika Serikat â€“ sumber hukum tertinggi negara. Dalam sistem federal Amerika Serikat, warga negara biasanya tunduk pada tiga tingkat pemerintahan, yaitu tingkat federal, negara bagian, dan pemerintah daerah. Tugas pemerintah daerah biasanya dibagi antara pemerintah county (setingkat kabupaten) dan munisipal. Secara umum, pejabat legislatif dan eksekutif dipilih melalui pemungutan suara pluralitas oleh warga negara menurut distrik. Tidak ada perwakilan proporsional di tingkat federal, begitu juga dengan tingkat di bawahnya. </p>
