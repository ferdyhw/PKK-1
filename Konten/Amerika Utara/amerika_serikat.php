            <p> Luas wilayah daratan utama Amerika Serikat adalah 2959064 mil persegi (7663941 km2). Alaska, yang dipisahkan dari daratan utama Amerika Serikat oleh Kanada, adalah negara bagian terluas, dengan luas 663268 mil persegi (1717856 km2). Hawaii berlokasi di tengah-tengah Samudra Pasifik, di sebelah barat daya Amerika Utara, dengan luas wilayah 10931 mil persegi (28311 km2). Amerika Serikat adalah negara terluas ketiga atau keempat di dunia (menurut luas daratan dan perairan), di bawah Rusia dan Kanada serta satu tingkat di atas atau di bawah RRC. Pemeringkatan tersebut bervariasi, tergantung pada apakah wilayah-wilayah yang dipersengkatan oleh RRC dan India turut dihitung dan bagaimana pengukuran luas total dari Amerika Serikat sendiri: kisaran perhitungan mulai dari 3676486 mil persegi (9522055 km2)[75] hingga 3717813 mil persegi (9629091 km2) dan 3794101 mil persegi .Jika yang dihitung hanya luas daratan saja, maka AS menempati peringkat ketiga, di bawah Rusia dan RRC serta di atas Kanada.</p>
            <p> Dengan luasnya yang besar dan keadaan geografis yang beragam, Amerika Serikat juga memiliki berbagai tipe iklim. Di sebelah timur meridian ke-100, iklimnya berkisar antara kontinental lembap di sebelah utara hingga subtropis lembap di sebelah selatan. Ujung selatan Florida beriklim tropis, begitu juga di Hawaii. Great Plains di sebelah barat meridian ke-100 beriklim semi-kering. Sedangkan sebagian besar pegunungan di bagian Barat beriklim alpen. Di Great Basin iklimnya kering, barat daya beriklim gurun, sementara pesisir California beriklim Mediterania, dan iklim laut terdapat di Oregon, Washington, dan Alaska selatan. Sebagian besar Alaska beriklim subarktik atau kutub. Cuaca ekstrem sering terjadi di negara-negara bagian yang berbatasan dengan Teluk Meksiko yang rentan terhadap badai; sebagian besar tornado di dunia terjadi di Amerika Serikat, terutama di Tornado Alley, Midwest. </p>

