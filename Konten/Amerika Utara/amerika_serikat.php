            Legislatif: Kongres dwidewan, yang terdiri dari Senat dan DPR. Tugasnya adalah membuat undang-undang federal, menyatakan perang, menyetujui perjanjian-perjanjian, menyetujui anggaran, dan memiliki kekuatan untuk meminta pertanggungjawaban pemerintah, yang bisa melengserkan seseorang dari kursi pemerintahan.<br>
