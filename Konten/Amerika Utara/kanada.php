            <p> Kanada menempati setengah dari utara Amerika Utara. Ia berbatasan dengan AS di selatan, dan di barat laut dengan Alaska. Negara ini terbentang dari Samudra Atlantik, dan Selat Davis di timur sampai ke Samudra Pasifik di barat; yang menjadi moto negara ini. Di utara terletak Laut Beaufort, dan Samudra Arktik; Tanah Hijau terletak di timur laut.
