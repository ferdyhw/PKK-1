            <h3> Politik</h3> 
            <p>Indonesia menjalankan pemerintahan republik presidensial multipartai yang demokratis. Seperti juga di negara-negara demokrasi lainnya, sistem politik di Indonesia didasarkan pada Trias Politika yaitu kekuasaan legislatif, eksekutif dan yudikatif. Kekuasaan legislatif dipegang oleh sebuah lembaga bernama Majelis Permusyawaratan Rakyat (MPR). </p>
            <p> MPR pernah menjadi lembaga tertinggi negara unikameral, namun setelah amendemen ke-4 MPR bukanlah lembaga tertinggi lagi, dan komposisi keanggotaannya juga berubah. MPR setelah amendemen UUD 1945, yaitu sejak 2004 menjelma menjadi lembaga bikameral yang terdiri dari 560 anggota Dewan Perwakilan Rakyat (DPR) yang merupakan wakil rakyat melalui Partai Politik, ditambah dengan 132 anggota Dewan Perwakilan Daerah (DPD) yang merupakan wakil provinsi dari jalur independen. Anggota DPR dan DPD dipilih melalui pemilu dan dilantik untuk masa jabatan lima tahun. Sebelumnya, anggota MPR adalah seluruh anggota DPR ditambah utusan golongan dan TNI/Polri. </p>
            <p> Lembaga eksekutif berpusat pada presiden, wakil presiden, dan kabinet. Kabinet di Indonesia adalah Kabinet Presidensial sehingga para menteri bertanggung jawab kepada presiden dan tidak mewakili partai politik yang ada di parlemen. Meskipun demikian, Walaupun ada presiden juga menunjuk sejumlah pemimpin partai politik untuk duduk di kabinetnya. Tujuannya untuk menjaga stabilitas pemerintahan mengingat kuatnya posisi lembaga legislatif di Indonesia. Namun pos-pos penting dan strategis umumnya diisi oleh menteri tanpa portofolio partai (berasal dari seseorang yang dianggap ahli dalam bidangnya) 
            Lembaga Yudikatif sejak masa reformasi dan adanya amendemen UUD 1945 dijalankan oleh Mahkamah Agung, Komisi Yudisial, dan Mahkamah Konstitusi, termasuk pengaturan administrasi para hakim. Meskipun demikian keberadaan Menteri Hukum dan Hak Asasi Manusia tetap dipertahankan.</p>   
