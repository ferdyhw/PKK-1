            <p>Kerajaan Thai merupakan tempat terletaknya beberapa wilayah geografis yang berbeda. Di sebelah utara, keadaannya bergunung-gunung, dan titik tertingginya berada di Doi Inthanon (2.576 m). Sebelah timur laut terdiri dari Hamparan Khorat, yang dibatasi di timur oleh sungai Mekong. Wilayah tengah negara didominasi lembah sungai Chao Phraya yang hampir seluruhnya datar, dan mengalir ke Teluk Thailand. Di sebelah selatan terdapat Tanah Genting Kra yang melebar ke Semenanjung Melayu. </p>

