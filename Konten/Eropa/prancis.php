            <h3>Geografi</h3>
            <p> Perancis adalah Negara yang terletak di Benua Eropa tepatnya di Eropa bagian Barat. Selain Teritori Metropolitan yang berada di Benua Eropa, Perancis juga memiliki beberapa pulau dan teritori di seberang laut yang berada di Benua lainnya. Luas wilayah Perancis secara keseluruhan adalah 643.801km2, luas wilayah ini termasuk Metropolitan Perancis seluas 551.500km2 dan Teritori seberang laut seluas 92.301km2. Teritori seberang laut tersebut diantaranya adalah French Guiana di Amerika Selatan, Guadeloupe dan Martinique di Laut Karabia Amerika Tengah, Mayotte dan Reunion di Samudera Hindia. </p>
            <p>Di Eropa, Metropolitan Perancis berbatasan dengan Swiss, Italia dan Jerman di sebelah Timur sedangkan sebelah Barat adalah Teluk Biscay. Di Utara Metropolitan Perancis adalah Selat Channel (selat yang memisahkan daratan Eropa dengan Inggris) dan Belgia. Di sebelah Selatannya, Metropolitas Perancis berbatasan dengan Spanyol dan Laut Mediterania. Secara Astronomis, Perancis terletak diantara 42°LU – 51°LU dan 5°BB – 8°BT. Prancis Metropolitan menempati wilayah seluas 547.030 kilometer persegi (211.209 sq mi), wilayah negara terluas di antara semua anggota Uni Eropa dan sedikit lebih besar dari Spanyol. Prancis memiliki berbagai macam lanskap, mulai dataran pantai di utara dan barat hingga jejaring pegunungan Alpen di tenggara, Massif Central di tengah-selatan dan Pyrenees di barat daya. </p>

