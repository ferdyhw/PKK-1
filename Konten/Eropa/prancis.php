            <h3> Politik</h3> 
            <p>Republik Prancis adalah sebuah republik semi-presidensial uniter dengan tradisi demokratis yang kuat. Konstitusi Republik Kelima disetujui melalui referendum tanggal 28 September 1958. Sehingga memperkuat kewenangan eksekutif dengan parlemen. Cabang eksekutif itu sendiri memiliki dua pemimpin: Presiden Republik, yang merupakan Kepala Negara dan dipilih langsung oleh hak pilih universal orang dewasa untuk jabatan selama 5 tahun (sebelumnya 7 tahun), dan Pemerintah, dipimpin oleh Perdana Menteri yang ditunjuk presiden.</p>

            <p>Parlemen Prancis adalah sebuah badan legislatif bikameral yang terdiri dari Majelis Nasional (Assemblée Nationale) dan Senat. Deputi Majelis Nasional mewakili konstituensi lokal dan terpilih langsung selama 5 tahun. Majelis memiliki kekuasaan untuk membubarkan kabinet, dan mayoritas anggota Majelis menetapkan pilihan pemerintah. Senator dipilih oleh dewan pemilih untuk jabatan 6 tahun (sebenarnya 9 tahun), dan setengah kursi dimasukkan dalam pemilihan setiap 3 tahun yang dimulai pada September 2008. Kekuasaan legislatif Senat terbatas; dalam penentangan antara kedua pihak, Majelis Nasional memiliki perkataan terakhir, kecuali untuk hukum konstitusional dan lois organiques (hukum yang disediakan langsung oleh konstitusi) dalam beberapa hal. Pemerintah memiliki pengaruh kuat dalam pembentukan agenda Parlemen.</p>
            
            <p>Politik Prancis ditandai oleh dua pengelompokkan yang saling menentang secara politik: pertama sayap kiri, dipusatkan di sekitar Partai Sosialis Prancis, dan lainnya sayap kanan, sebelumnya dipusatkan pada Rassemblement pour la République (RPR) dan sekarang Persatuan untuk Gerakan Rakyat (UMP). Cabang eksekutif kebanyakan terdiri dari anggota UMP. </p>
