            <h3> Politik</h3> 
            <p> Sampai tahun 1917 Rusia merupakan kerajaan/kekaisaran dengan seorang tsar sebagai kepala negara. Selama masih kerupakan kekaisaran, terutama pada masa Dinasti Romanov, Rusia mengalami persinggungan politik dengan negara-negara Eropa, di antaranya konflik dengan pemerintahan Prancis pimpinan Napoleon Bonaparte, Krisis Balkan karena menginginkan pelabuhan yang bebas dari es di Eropa yang dinamakan Politik Air Hangat, Penyatuan Pan Slavia serta sering mengalami pertempuran dengan Turki Usmani (Ottoman) Turki dalam memperebutkan wilayah Kaukasus dan Austria-Hungaria dalam Perang Dunia I. Akibat politik ini pula terjadi pertempuran dengan Jepang dan intervensi terhadap Tiongkok. Masa selanjutnya, politik Rusia dilebur dengan kepentingan Uni Soviet yang mengambil sikap independen bahkan menentang ketika terjadi penggulingan kekuasaan Mikhail Gorbachev oleh Gennady Yanayev menjelang keruntuhan Uni Soviet yang diprakarsai Presiden Boris Yeltsin. Pemerintahan dipegang oleh presiden yang berpusat di Kremlin serta perdana menteri yang bertanggung jawab terhaadap parlemen namun dengan peranan yang terbatas dibandingkan dengan Presiden. Sejak pembangkangan Wakil Presiden Aleksander Ruskoi dan ketua parlemen asal Chechnya, Ruslan Khasbulatov, lembaga wakil presiden dihapus. </p>

            <p> Parlemen memiliki dua kamar, yakni Majelis Federal (Федеральное Собрание; Federalnoye Sobraniye) yang merupakan majelis tinggi dan majelis rendah yang dikenal dengan Duma. Karena Rusia merupakan negara federal yang memiliki berbagai macam etnis, setelah keruntuhan Uni Soviet, Rusia mengalami masalah separatisme. Ada beberapa kelompok etnis yang ingin memisahkan diri dan mengakibatkan krisis berlarut-larut, seperti di Chechnya dan Ingushetia.
            Pemerintahan Rusia dapat dibagi menjadi:Masa Tsar atau Kekaisaran, Masa Uni Soviet, Masa Kepresidenan Rusia </p>
