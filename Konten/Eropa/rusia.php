            <h3>Geografi</h3>
            <p> Rusia adalah negara terbesar di dunia; total luas wilayahnya mencapai 17.125.200 kilometer persegi (6612100 sq mi). Ada 23 Situs Warisan Dunia UNESCO di negara ini, 40 cadangan biosfer UNESCO, 41 taman nasional dan 101 cadangan alam. Wilayahnya membentang pada 41° sampai 82° N lintang utara, dan 19° bujur timur sampai 169° bujur barat. Perluasan teritorial Rusia sebagian besar didapatkan pada akhir abad ke-16 di bawah Yermak Timofeyevich selama pemerintahan Ivan IV Vasilyevich, waktu ketika negara-negara di bagian barat Rusia bersatu bersama membentuk satu negara. Yermak membentuk tentara dan berhasil mendapatkan hampir semua wilayah yang awalnya merupakan daerah Kekaisaran Mongol, mengalahkan pemimpinnya Khan Kuchum. </p>
            <p> Wilayah Rusia berada pada benua Eropa, khususnya Eropa Timur serta benua Asia di mana Pegunungan Ural menjadi batas antara kedua benua. Wilayah paling luas adalah Siberia yang umumnya beriklim tundra. Karena letaknya di belahan bumi yang paling utara, maka wilayah perairan Rusia umumnya tertutupi es dengan beberapa laut yang bebas es yakni Laut Barents, Laut Putih, Laut Kara, Laut Laptev dan Laut Siberia Timur yang merupakan bagian dari Arktik atau kutub utara, serta Laut Bering, Laut Okhotsk dan Laut Jepang yang merupakan bagian dari Samudra Pasifik. Iklim di sebagian besar wilayah Rusia adalah Tundra dengan musim dingin yang panjang dan membekukan dan musim panas yang sejuk dan pendek. Rusia memiliki beberapa pulau, antara lain Novaya Zemlya, daratan Franz-Josef, kepulauan Siberia Baru, pulau Wrangel di Samudra Arktik, Kepulauan Kuril dan Sakhalin (yang masih dipersengketakan dengan Jepang). Rusia memiliki beberapa sungai, di antaranya Sungai Dnieper (perbatasan degan Ukraina) dan Sungai Volga. Selain itu terdapat Laut Kaspia serta Laut Hitam yang berbatasan dengan Turki. Melalui Selat Bosphorus dan Selat Dardanela, kapal-kapal Rusia dari Laut Hitam dapat berlayar menuju Laut Tengah dan Terusan Suez.</p>

