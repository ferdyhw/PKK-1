            <h3>Geografi</h3>
            <p> Austria adalah sebuah negara Republik Federal yang terletak di Benua Eropa, tepatnya di Eropa Tengah. Negara yang terkurung daratan ini berbatasan dengan Slowakia dan Ceko di sebelah timurnya, Swiss dan  Liechtenstein di sebelah baratnya. Sedangkan di sebelah Utara Austria berbatasan dengan Jerman dan Ceko, Austria juga berbatasan dengan Italia dan Slovenia disebelah selatannya. Secara Astronomis, Austria berada diantara  46°-49°LU, and 9°-18°BT. </p>

            <p>Austria memproklamasikan negaranya menjadi sebuah negara Republik pada tahun 1918. Namun pada tahun 1934, Austria menjadi negara sistem diktator oleh Engelbert Dollfuss. Tahun 1918 kemudian diperingati sebagai hari kemerdekaannya. Austria diduduki oleh rezim Nazi pada tahun 1938 hingga kekalahan Jerman pada Perang Dunia ke-2.  Setelah Perang Dunia ke-2 tersebut, administrasi Austria diambil alih oleh pihak sekutu hingga pada tahun 1955 Austria diberikan kemerdekaan dengan syarat untuk tetap netral dan tidak memihak ke kekuatan Barat. Austria mulai aktif dan semakin dekat dengan Eropa setelah jatuhnya komunisme Eropa pada tahun 1989. Austria bergabung dengan Uni Eropa pada tahun 1995 dan secara resmi menggunakan mata uang Euro pada tahun 1999.</p>

            <p>Luas wilayah Austria adalah sebesar 83.871 km2 dengan jumlah penduduk sebanyak 8.754.413 jiwa. Mayoritas penduduk Austria adalah etnis Austria (sekitar 91,1%) dan sebagian besarnya memeluk agama Katolik yaitu sebanyak 73,8%. Bahasa resmi Austria adalah Bahasa Jerman. Bagian barat dan selatan Austria terletak di Pegunungan Alpen menjadikannya destinasi menarik untuk olahraga musim dingin. Gunung yang tertinggi di Austria ialah Grossglockner setinggi 3.798 m dari permukaan laut dan diikuti dengan Wildspitze (3.774 m). Bagian utara dan timur kebanyakkan kawasan berbukau. Iklim Austria bersifat sederhana dan dingin (temperate climate). </p>

