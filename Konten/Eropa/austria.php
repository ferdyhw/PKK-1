            <h3> Politik</h3> 
            <p>Austria menjadi republik federal, demokrasi parlementer melalui Konstitusi Federal 1920. Diperkenalkan kembali pada 1945 kepada 9 negara bagian Republik Federal. Kepala Negara ialah Presiden Federal, yang dipilih secara langsung. Ketua Pemerintahan Federal ialah Kanselir Federal, yang diangkat presiden. Pemerintahan bisa dihapus dari posisi oleh dekret presiden maupun mosi tidak percaya di kamar parlemen yang lebih rendah, Nationalrat. </p>

            <p>Parlemen Austria terdiri atas 2 kamar. Susunan Nationalrat ditentukan tiap 4 tahun oleh Pemilu bebas yang mana tiap warga negara diizinkan memilih untuk mengisi ke-183 kursinya. "Rintangan Empat Persen" mencegah perpecahan besar pada kancah politik di Nationalrat dengan menghadiahi kursi hanya kepada ParPol yang telah mendapat sedikitnya 4% permulaan pemilu, atau dengan pilihan lain, telah memenangkan kursi langsung, atau Direktmandat, di salah satu dari 43 distrik pemilihan regional. Nationalrat ialah kamar dominan dalam pembentukan badan legislatif di Austria. Bagaimanapun, Majelis Parlemen Atas, Bundesrat memiliki hak veto terbatas. Konvensi, disebut Österreich–Konvent diadakan pada 30 Juni 2003 untuk memutuskan usulan mereformasi konstitusi, namun telah gagal mengajukan usulan yang akan menerima dua pertiga suara di Nationalrat yang perlu untuk amendemen dan/atau reformasi konstitusional. Bagaimanapun beberapa bagian penting laporan akhir umumnya disetujui dan tetap diharapkan untuk diwujudkan. </p>
