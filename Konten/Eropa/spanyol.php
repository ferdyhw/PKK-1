            <p> Spanyol adalah sebuah negara yang terletak di semenanjung Iberia Benua Eropa, tepatnya di Eropa bagian barat daya. Negara yang memiliki nama lengkap Kerajaan Spanyol (Kingdom of Spain) ini hanya berbatasan darat dengan dua negara yaitu berbatasan dengan Portugal di sebelah barat dan berbatasan dengan Perancis di sebelah Timur Laut. Sebelah Utara Spanyol adalah Samudera Atlantik sedangkan Selatannya adalah Laut Tengah (Laut Mediterania) yang memisahkan Spanyol dengan Negara-negara Afrika seperti Maroko dan Aljazair.</p>
            <p>Spanyol merupakan negara terbesar ketiga di benua Eropa dengan luas wilayahnya sebesar 505.370 km². Selain di Benua Eropa, Spanyol juga memiliki beberapa wilayah di benua Afrika (kota Ceuta dan Melilla) dan Samudera Atlantik (Kepulauan Canary) serta beberapa pulau di Laut Tengah. Jumlah penduduk Spanyol sebanyak 48.563.476 jiwa dengan mayoritas penduduknya beragama Katolik Roma yaitu sebanyak 67,8% dari keseluruhan populasinya. Ibukota Spanyol adalah Kota Madrid.</p>
