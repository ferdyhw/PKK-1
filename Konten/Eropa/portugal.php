            <p> Portugal adalah negara republik dengan Lisbon sebagai ibu kota negara. Empat komponen utama dalam pemerintahan Portugal adalah: Presiden sebagai kepala negara, Parlemen, Perdana Menteri sebagai kepala pemerintahan, dan mahkamah tinggi. Konstitusi membagi kekuasaan tersebut menjadi eksekutif, legislatif dan yudikatif. Presiden dipilih setiap lima tahun sekali, sementara Perdana Menteri dipilih langsung oleh presiden, dan umumnya merupakan pemimpin dari partai pemenang pemilu. Lembaga legislatif terdiri atas 230 orang deputi yang akan menjabat selama 4 tahun. Sementara mahkamah terdiri dari yudisial, administratif, dan fiskal.</p>
            
