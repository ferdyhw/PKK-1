            <h3> Politik </h3>
            <p>Pemerintah Filipina mengikuti Pemerintah Amerika Serikat. Dia ditata sebagai sebuah republik, di mana Presiden berfungsi sebagai kepala negara, kepala pemerintahan, dan Panglima Tertinggi angkatan bersenjata. Presiden dipilih dalam pemilu untuk masa jabatan 6 tahun, dan memilih, dan mengepalai kabinet. Dewan Legislatif Filipina mempunyai dua kamar: Kongres terdiri dari Senat, dan Dewan Perwakilan; anggota keduanya dipilih oleh pemilu. Ada 24 senator yang menjabat selama 6 tahun di Senat, sedangkan Dewan Perwakilan terdiri dari tidak lebih dari 250 anggota kongres yang melayani selama 3 tahun. Cabang yudikatif pemerintah dikepalai oleh Mahkamah Agung, yang memiliki seorang Ketua Mahkamah Agung sebagai kepalanya, dan 14 Hakim Agung, semuanya ditunjuk oleh Presiden. </p>

            <p>Filipina merupakan anggota aktif dari PBB sejak penerimaannya pada 24 Oktober 1945. Filipina juga merupakan negara pendiri ASEAN, dan merupakan pemain aktif dalam APEC, Uni Latin, dan anggota dari Grup 24. Filipina juga merupakan sekutu Amerika Serikat, tetapi juga merupakan anggota dari Gerakan Non-Blok. </p>

            <p>Filipina bersengketa dengan Republik Tiongkok (Taiwan), Republik Rakyat Tiongkok, Vietnam, dan Malaysia atas minyak, dan gas alam di Kepulauan Spratly, dan Scarborough Shoa, dan dengan Malaysia atas Sabah. Sultan Sulu yang menerima Sabah sebagai hadiah pada 1703 setelah menolong Sultan Brunei mengalahkan pemberontak, telah memberikan Pemerintah Filipina kuasa untuk mengklaim wilayahnya yang hilang. Sampai saat ini, keluarga Sultan Sulu masih menerima pembayaran "sewa" untuk Sabah dari Pemerintah Malaysia. </p>
