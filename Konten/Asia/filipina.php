            <h3>Geografi</h3>
            <p> Filipina tediri dari 7.107 pulau dengan luas total daratan diperkirakan 343.448 km². Negara ini terletak antara 116° 40', dan 126° 34' BT, dan 4° 40', dan 21° 10' LU. Di timur dia berbatasan dengan Laut Filipina, di barat dengan Laut Tiongkok Selatan, dan di selatan dengan Laut Sulawesi. Pulau Borneo terletak beberapa ratus kilometer di barat daya, dan Taiwan di utara. Maluku, dan Sulawesi di selatan, dan di timur adalah Palau. Filipina memiliki garis pantai sepanjang 36.289 km (22.549 mil) yang menjadikannya negara dengan garis pantai terpanjang kelima di dunia. </p>

