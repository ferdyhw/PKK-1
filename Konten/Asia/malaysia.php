            <h3>Geografi</h3>
            <p> Malaysia adalah negara berpenduduk terbanyak ke-43 dan negara dengan daratan terluas ke-66 di dunia, dengan jumlah penduduk kira-kira 27 juta dan luas wilayah melebihi 320.000 km2. Jumlah penduduk sedemikian cukup sebanding dengan jumlah penduduk Arab Saudi dan Venezuela, dan luas wilayah sedemikian sebanding dengan luas wilayah Norwegia dan Vietnam, atau New Mexico, sebuah negara bagian di Amerika Serikat. </p>

            <p>Malaysia terdiri atas dua kawasan utama yang terpisah oleh Laut Tiongkok Selatan. Keduanya memiliki bentuk muka bumi yang hampir sama, yaitu dari pinggir laut yang landai hingga hutan lebat dan bukit tinggi. Puncak tertinggi di Malaysia (dan juga di Kalimantan) yaitu Gunung Kinabalu setinggi 4.095,2 meter di Sabah. Iklim lokal adalah khatulistiwa dan dicirikan oleh angin muson barat daya (April hingga Oktober) dan timur laut (Oktober hingga Februari). Tanjung Piai, terletak di selatan negara bagian Johor, adalah tanjung paling selatan benua Asia. Selat Malaka, terletak di antara Sumatra dan Semenanjung Malaysia, jalur pelayaran terpenting di dunia. </p>

            <p> Kuala Lumpur adalah ibu kota resmi dan kota terbesar di Malaysia. Putrajaya di pihak lain, dipandang sebagai ibu kota administratif pemerintahan persekutuan Malaysia. Meskipun banyak cabang eksekutif dan judikatif pemerintahan persekutuan telah pindah ke sana (untuk menghindari kemacetan yang tumbuh di Kuala Lumpur), tetapi Kuala Lumpur masih dipandang sebagai ibu kota legislatif Malaysia karena di sanalah beradanya kompleks gedung Parlemen Malaysia. Kuala Lumpur juga merupakan pusat perdagangan dan keuangan Malaysia. Kota utama lain termasuk Ipoh, George Town, Johor Bahru, Kuching, Kota Kinabalu, Miri, Alor Star, Kota Melaka, dan Petaling Jaya. </p>

