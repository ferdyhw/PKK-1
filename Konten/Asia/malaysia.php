            <h3> Politik</h3>
            <p>Federasi Malaysia adalah sebuah monarki konstitusional. Kepala negara persekutuan Malaysia adalah Yang di-Pertuan Agong, biasa disebut Raja Malaysia. Yang di-Pertuan Agong dipilih dari dan oleh sembilan Sultan Negeri-Negeri Malaya, untuk menjabat selama lima tahun secara bergiliran; empat pemimpin negeri lainnya, yang bergelar Gubernur, tidak turut serta di dalam pemilihan. Sistem pemerintahan di Malaysia bermodelkan sistem parlementer Westminster, warisan Penguasa Kolonial Britania. Tetapi di dalam praktiknya, kekuasaan lebih terpusat di eksekutif daripada di legislatif, dan judikatif diperlemah oleh tekanan berkelanjutan dari pemerintah selama zaman Mahathir, kekuasaan judikatif itu dibagikan antara pemerintah persekutuan dan pemerintah negara bagian. </p>

            <p> Kekuasaan legislatur dibagi antara legislatur persekutuan dan legislatur negeri. Parlemen bikameral terdiri dari dewan rendah, Dewan Rakyat (mirip "Dewan Perwakilan Rakyat" di Indonesia) dan dewan tinggi, Senat atau Dewan Negara (mirip "Dewan Perwakilan Daerah" di Indonesia). 222 anggota Dewan Rakyat dipilih dari daerah pemilihan beranggota-tunggal yang diatur berdasarkan jumlah penduduk untuk masa jabatan terlama 5 tahun. </p> 

            <p>Kekuasaan eksekutif dilaksanakan oleh kabinet yang dipimpin oleh perdana menteri; konstitusi Malaysia menetapkan bahwa perdana menteri haruslah anggota dewan rendah (Dewan Rakyat), yang direstui Yang di-Pertuan Agong dan mendapat dukungan majoritas di dalam parlemen. Kabinet dipilih dari para anggota Dewan Rakyat dan Dewan Negara dan bertanggung jawab kepada badan itu. Sedangkan kabinet merupakan anggota parlemen yang dipilih dari Dewan Rakyat atau Dewan Negara. </p>
