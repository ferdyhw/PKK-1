            <h3> Politik</h3> 
            <p> Republik Chili adalah sebuah negara kesatuan demokratis dengan sistem presidensial, terdiri dari berbagai institusi yang otonom seperti yang dikehendaki konstitusi dengan fungsi-fungsi tertentu dan dengan kompetensi yang terdistribusi baik di antara organ-organ negara, yang tidak sama dengan doktrin tradisional pemisahan kekuasaan. Konstitusi terkini Chili disetujui melalui sebuah referendum nasional pada tanggal 11 September 1980, di bawah pemerintahan militer Augusto Pinochet. Konstitusi ini, yakni konstitusi ke-10 dalam sejarah konstitusi Chili, telah menjadi dokumen fundamental negara sejak tanggal 11 Maret 1981. </p>
