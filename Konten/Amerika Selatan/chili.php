            <p> Chili adalah sebuah negara yang terletak di benua Amerika Selatan. Negara yang memiliki nama resmi Republik Chili ini secara astronomisnya berada diantara 17°LS – 56°LS dan 56°BB – 66°BB. Secara geografis, Chili berbatasan dengan Peru di sebelah utaranya dan berbatasan dengan Bolivia di sebelah timur laut, sedangkan disebelah timurmya Chili berbatasan dengan Argentina. Disebelah barat dan selatan adalah Samudera Pasifik. </p>
