            <h3>Geografi</h3>
            <p> Argentina adalah sebuah negara yang terletak di benua Amerika Selatan. Argentina yang memiliki luas wilayah sebesar 2.780.400km2 ini juga merupakan salah satu negara terbesar di dunia yaitu menduduki urutan ke-8 sebagai negara terbesar di dunia. Jumlah penduduk Argentina adalah sebanyak 44.293.293 jiwa yang mayoritasnya adalah bangsa Eropa (kebanyakan adalah bangsa Italia dan Spanyol) dan bangsa Mestizo (campuran Eropa dan Indian Amerika). Bahasa Spanyol adalah bahasa resmi negara yang mempunyai nama lengkap Republik Argentina ini. Nama Argentina berasal dari bahasa latin “argentum” yang artinya adalah “perak”. </p>
