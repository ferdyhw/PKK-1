            <h3> Politik</h3> 
            <p> Konstitusi Argentina 1853, yang diubah pada 1994, memberi mandat pembagian kekuasaan antara badan-badan eksekutif, legislatif dan kehakiman di tingkat nasional dan negara bagian. Presiden dan Wakil Presiden dipilih secara langsung setiap 4 tahun. Mereka boleh menyandang jabatan selama tidak lebih 2 masa jabatan; mereka hanya diizinkan bertarung untuk kali ketiga setelah tidak aktif selama satu masa. Presiden melantik anggota kabinet dan konstitusi memberikan kekuasaan besar kepadanya sebagai kepala negara dan kepala pemerintahan, termasuk kuasa untuk mengubah undang-undang di bawah mandat presiden dengan syarat "mendesak dan perlu" dan juga hak veto yang terbatas.
            <p>Parlemen Argentina dikenal sebagai Kongres Nasional atau Congreso Nacional. Menggunakan sistem Parlemen bikameral terbagi atas dua kamar yang utama: Senat atau Senado dengan 72 kursi dan Dewan Perwakilan atau Cámara de Diputados dengan 257 anggota. Sejak 2001, para senator dipilih secara langsung, dengan setiap negara bagian, termasuk ibu kota Federal, Buenos Aires, diwakili oleh tiga senator. Para senator menjabat selama 6 tahun. Sepertiga anggota Senat akan bertanding sekali lagi setiap 2 tahun. Anggota Dewan Perwakilan dipilih secara langsung dan menjabat selama 4 tahun. Rakyat memilih separuh dari anggota Dewan Perwakilan setiap dua tahun dan setiap anggota dewan dipilih mengikuti sistem pemilu perwakilan seimbang. Sepertiga dari semua calon yang diajukan oleh partai-partai harus terdiri dari kaum perempuan.</p>
            <p>Lembaga yudikatifnya bebas dari lembaga eksekutif dan legislatif. Mahkamah Agung Argentina mempunyai 9 anggota, yang diangkat oleh Presiden atas persetujuan Senat. Sisa hakim-hakimnya ditunjuk oleh Consejo de la Magistratura de la Nación (Dewan Kehakiman Nasional), sebuah sekretariat yang terdiri atas wakil-wakil para hakim, pengacara, Kongres, dan pihak Eksekutif.</p>
