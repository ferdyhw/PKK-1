            <p> Peru (Spanyol: Perú; Quechua: Piruw Republika; Aymara: Piruw Suyu), secara resmi Republik Peru (Spanyol: República del Perú), adalah sebuah negara di Amerika Selatan bagian barat. Peru merupakan negara yang kaya dalam budaya antropologi dan dikenal sebagai tempat lahir Kerajaan Inca yang beribu kota di Machu Picchu. </p>
