            <h3> Politik</h3> 
            <p>Berdasarkan Undang Undang Dasar negara Peru, presiden dapat terpilih kembali sebanyak satu kali. Kemudian, dapat terpilih kembali setelah berselang satu kali. Selain presiden, di negara Peru juga terdapat wakil presiden yang pertama dan kedua untuk mengadakan parlemen tunggal. Lebih lanjut, pada tahun 2000, Kongres Peru menerima amandemen Undang – Undang Dasar. Di mana dalam amandemen tersebut, menetapkan bahwa presiden tidak bisa terpilih kembali. Selain tidak berhaknya presiden untuk terpilih kembali, dalam amandemen tersebut juga mengubah nama parlemen menjadi kongres. Kongres memiliki hak untuk melakukan pengawasan dan legislasi. Kemudian, anggota parlemen dapat dipilih dalam pemilihan dengan masa jabatan lima tahun, dan dapat terpilih kembali.</p>
