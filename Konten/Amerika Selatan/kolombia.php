            <p>Pemerintahan Kolombia mengambil bentuk kepresidenan demokrasi perwakilan republik yang ditetapkan oleh Konstitusi Kolombia tahun 1991. Pemerintahan Kolombia terbagi tiga kekuasaan; eksekutif, legislatif dan yudikatif dengan lembaga kontrol, dan lembaga pemilihan khusus. Presiden Kolombia adalah puncak eksekutif tertinggi di Kolombia, dan juga sebagai kepala negara dan kepala pemerintahan dengan kekuasaan administratif, diikuti oleh Wakil Presiden dan Kementrian Kolombia. </p>
