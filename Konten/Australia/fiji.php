            <p>  Fiji atau lebih lengkap disebut Republik Fiji adalah sebuah negara kepulauan yang terletak di bagian selatan Samudera Pasifik atau sering disebut dengan Kawasan Oseania. Kepulauan Fiji yang berada di timur laut dan berjarak sekitar 2.000km dari Pulau Utara (North Island) Selandia Baru ini juga berbatasan laut dengan Vanuatu disebelah timurnya,  berbatasan laut dengan Tonga disebelah barat dengan berbatasan laut dengan Tuvalu disebelah selatannya. Kepulauan Fiji memiliki sekitar 322 pulau, namun hanya sekitar 106 pulau yang berpenghuni. Dua pulau utama Fiji diantaranya adalah pulau Viti Levu dan pulau Vanua Levu. Sekitar 82% dari keseluruhan populasi Fiji berada di kedua pulau utama tersebut. Ibukota Fiji adalah kota Suva yang terletak di pulau Viti Levu. </p>
