            <h3> Politik</h3> 
            <p>Selandia Baru adalah monarki konstitusional dengan demokrasi parlementer, meskipun konstitusinya tidaklah tertulis.Ratu Elizabeth II adalah kepala negara yang diberi gelar Ratu Selandia Baru. Ratu diwakili oleh Gubernur Jenderal, yang ditunjuk oleh Ratu atas nasihat Perdana Menteri. Gubernur Jenderal dapat menjalankan hak prerogatif mahkota (seperti meninjau kasus-kasus ketidakadilan, dan mengangkat Dewan Menteri (kabinet), duta besar, dan pejabat publik penting lainnya) dan dalam situasi yang langka, kekuasaan cadangan (kekuasaan untuk memberhentikan Perdana Menteri, membubarkan Parlemen, atau menolak Persetujuan Kerajaan atas sebuah rancangan undang-undang menjadi undang-undang). Kekuasaan Ratu, dan Gubernur Jenderal dibatasi oleh kekakuan konstitusional, dan mereka biasanya tidak dapat dijalankan tanpa nasihat dari Kabinet. </p>
            <p> Parlemen Selandia Baru memegang kekuasaan legislatif dan terdiri dari Yang Berdaulat (diwakili oleh Gubernur Jenderal) dan Dewan Perwakilan Rakyat. Parlemen juga pernah meliputi sebuah majelis tinggi, Dewan Legislatif, hingga dewan ini dihapuskan pada tahun 1950. Kedudukan tertinggi Parlemen berada pada Yang Berdaulat, dan berada di Inggris menurut Bill of Rights 1689 dan telah diratifikasi sebagai undang-undang di Selandia Baru. Dewan Perwakilan Rakyat dipilih secara demokratis, dan Pemerintah dibentuk dari partai atau koalisi yang menguasai mayoritas kursi di dewan.</p>
