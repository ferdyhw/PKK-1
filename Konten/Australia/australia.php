            <h3> Politik</h3> 
            <p>Australia adalah monarki konstitusional dengan pembagian kekuasaan federatif. Pemerintah Australia menganut sistem parlementer dengan Ratu Elizabeth II sebagai puncak kepemimpinannya, yakni sebagai Ratu Australia, suatu peran yang berbeda dengan kedudukannya sebagai ratu bagi Dunia Persemakmuran lainnya. Ratu menetap di Britania Raya, dan dia diwakili oleh utusan yang menetap di Australia, (Gubernur Jenderal pada level federal, dan oleh Gubernur pada level negara bagian), yang menurut konvensi bertindak menurut nasihat menteri-menterinya. Otoritas eksekutif tertinggi berada pada Konstitusi Australia, tetapi kekuasaan untuk menjalankannya diserahkan -menurut konstitusi- kepada Gubernur Jenderal.[138][139] Pelaksanaan kekuasaan cadangan Gubernur Jenderal di luar permintaan Perdana Menteri adalah pembubaran Pemerintah Whitlam ketika terjadi krisis konstitusional 1975.</p>
            <p> Terdapat tiga cabang pemerintahan di Australia: <br>
            Legislatur: Parlemen Australia yang terdiri dari Gubernur-Jenderal, Senat, dan Dewan Perwakilan. <br>
            Eksekutif: Dewan Eksekutif Federal; praktisnya adalah Gubernur-Jenderal yang dinasihati oleh Perdana Menteri, dan Menteri-Menteri Negara. <br>
            Judisial: Mahkamah Agung Australia, dan pengadilan-pengadilan federal lainnya, yang para hakimnya diangkat oleh Gubernur-Jenderal berdasarkan nasihat Dewan. </p> 
