            <h3> Politik</h3> 
            <p>Sistem Pemerintahan yang dianut oleh Mesir adalah sistem pemerintahan Republik Presidensil, yaitu sistem pemerintahan dengan kepala negaranya adalah seorang Presiden yang dipilih oleh setiap 4 tahun sekali dan dapat menjabat sebanyak 2 kali periode. Berbeda dengan Republik Presidensil pada umumnya, Kepala pemerintahan di Mesir adalah seorang Perdana Menteri yang diangkat oleh Presiden. </p>
            <p> Mesir berbentuk republik sejak 18 Juni 1953, Mesir adalah negara pertama yang mengakui kedaulatan Indonesia. Muhammad Husni Mubarak telah menjabat sebagai Presiden Mesir selama lima periode, sejak 14 Oktober 1981 setelah pembunuhan Presiden Muhammad Anwar Sadat. Selain itu, ia juga pemimpin Partai Demokrat Nasional. Perdana Menteri Mesir, Dr. Ahmad Nazhif dilantik pada 9 Juli 2004 untuk menggantikan Dr. Athaf Ubaid. Kekuasaan di Mesir diatur dengan sistem semipresidensial multipartai. Secara teoretis, kekuasaan eksekutif dibagi antara presiden dan perdana menteri namun dalam praktiknya kekuasaan terpusat pada presiden, yang selama ini dipilih dalam pemilu dengan kandidat tunggal. Mesir juga mengadakan pemilu parlemen multipartai. Pada akhir Februari 2005, Presiden Mubarak mengumumkan perubahan aturan pemilihan presiden menuju ke pemilu multikandidat. Untuk pertama kalinya sejak 1952, rakyat Mesir mendapat kesempatan untuk memilih pemimpin dari daftar berbagai kandidat. Namun, aturan yang baru juga menerapkan berbagai batasan sehingga berbagai tokoh, seperti Aiman Nur, tidak bisa bersaing dalam pemilihan dan Mubarak pun kembali menang dalam pemilu.</p>

