            <h3>Geografi</h3>
            <p> Mesir atau dalam bahasa Inggris disebut dengan Egypt adalah sebuah negara yang terletak di dua benua (trans-benua) yaitu benua Afrika dan benua Asia. Negara dengan nama lengkap Republik Arab Mesir ini sebagian besar wilayahnya terletak di bagian timur laut Afrika sehingga sering digolongkan sebagai negara yang berada di benua Afrika.Secara geografis, Mesir yang memiliki luas wilayah 1.001.450 km2 ini berbatasan dengan Libya di sebelah barat dan Sudan di sebelah Selatan, sedangkan di sebelah timur Mesir adalah Jalur Gaza dan Israel. Mesir juga berbatasan laut dengan Laut Tengah di sebelah utara dan Laut Merah di sebelah timur. Secara Astronomis, Mesir terletak di antara  25째BT- 36째BT dan 22째LU- 32째LU. Ibukota Mesir berada di Kota Kairo. </p>
            <p> Mesir adalah negara ke-30 terbesar di dunia. Karena iklimnya yang ekstrem dan gersang, sebagian besar populasi terkonsentrasi di sepanjang Lembah Nil yang sempit dan delta Sungai Nil. Sekitar 99 % penduduk Mesir menempati kawasan lembah sungai Nil dan kawasan delta sungai di dekat Laut Mediterania. Terlepas dari Lembah Nil, mayoritas lanskap Mesir adalah gurun, dengan beberapa oasis bertebaran di kawasan itu. Tiupan angin yang kencang menciptakan banyak bukit pasir yang tingginya sampai 30 m (100 kaki). Mesir termasuk bagian dari gurun Sahara dan Gurun Libya. Gurun ini dilindungi Kerajaan Firaun dari ancaman Barat dan disebut sebagai "tanah merah" pada zaman Mesir kuno.</p>
