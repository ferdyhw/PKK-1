            <h3>Geografi</h3>
            <p> Maroko adalah sebuah negara kerajaan yang terletak di benua Afika bagian Utara. Negara yang secara astronomis berada diantara  27°-36°LU dan  1°-14°BB ini berbatasan darat dengan Aljazair disebelah timur dan selatannya sedangkan di sebelah barat dayanya berbatasan Sahara Barat. Sebelah utara Maroko adalah selat Gibraltar yang memisahkannya dengan Spanyol dan daratan Eropa sedangkan di sebelah Baratnya adalah Samudera Atlantik. Negara yang memiliki nama lengkap Kerajaan Maroko ini mengakui wilayah Sahara Barat sebagai salah satu Provinsinya sejak tahun 1975, namun sebagian besar negara di dunia Internasional ini tidak mengakui pengakuan ini.</p>
            <p> Secara geografis, memiliki wilayah yang sebagian besar terdiri dari gurun dan pegunungan yang terjal. Ia merupakan salah satu dari hanya tiga negara (dengan Spanyol dan Prancis) yang memiliki garis pantai di Samudra Atlantik dan juga di Laut Mediterania. Maroko memiliki populasi lebih dari 33.800.000 dan luas 446.550 km2 (172.410 sq mi). Ibu kotanya adalah Rabat. Kota terbesar adalah Casablanca. Kota-kota besar lainnya adalah Marrakesh, Tangier, Tetouan, Salé, Fes, Agadir, Meknes, Oujda, Kenitra, dan Nador. </p>

