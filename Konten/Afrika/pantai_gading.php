            <h3>Geografi</h3>
            <p> Pantai Gading adalah negara bagian barat sub-Sahara Afrika. Ini berbatasan dengan Liberia dan Guinea di barat, Mali dan Burkina Faso di utara, Ghana di timur, dan Teluk Guinea (Samudera Atlantik) di selatan. Negara terletak di antara garis lintang 4 ° dan 11 ° LU, dan garis bujur 2 ° dan 9 ° W. Sekitar 64,8% dari tanah adalah lahan pertanian, tanah yang subur mengambil 9,1%, padang rumput permanen dengan 41,5%, dan tanaman permanen menempati 14,2%. Polusi air adalah salah satu masalah terbesar yang dihadapi negara ini. Ibukota Pantai Gading, Yamoussoukro, adalah rumah bagi gedung gereja terbesar di dunia, Basilika Our Lady of Peace of Yamoussoukro. </p>
            <p>Sejak 2011, Pantai Gading telah dikelola secara administratif menjadi 12 distrik plus dua kota otonom tingkat kabupaten. Kabupaten dibagi menjadi 31 wilayah ; daerah dibagi menjadi 108 departemen ; dan departemen dibagi menjadi 510 sub-prefektur . Dalam beberapa kasus, beberapa desa diorganisir menjadi komune . Distrik otonom tidak dibagi menjadi beberapa wilayah, tetapi distrik tersebut memang berisi departemen, sub-prefektur, dan komune. Sejak 2011, gubernur untuk 12 kabupaten non-otonom belum ditunjuk, dan sebagai hasilnya kabupaten ini belum mulai berfungsi sebagai entitas pemerintah.</p>

