            <p>Negara memiliki nama lengkap Republik Afrika Selatan (Republic of South Africa) ini menganut sistem pemerintahan Republik yang kepala negara dan kepala pemerintahannya adalah seorang Presiden. Presiden Afrika Selatan ini dipilih oleh Majelis Nasional (National Assembly) dan Majelis Provinsi-provinsi Nasional (National Council of Provinces) sedangkan anggota Majelis Nasional dan Majelis Provinsi-provinsi Nasional ini dipilih melalui Pemilihan Umum yang diselenggarakan setiap 5 tahun sekali. Ibukota Afrika Selatan adalah Pretoria (eksekutif), Cape Town (Legislatif) dan Bloemfontein (yudikatif). </p>
