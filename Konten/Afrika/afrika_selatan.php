            <h3>Geografi</h3>
            <p>  Afrika Selatan adalah sebuah negara yang terletak di bagian paling selatan benua Afrika. Negara yang secara astronomis berada di antara  23⁰ LS – 35⁰ LS dan 18⁰ BT – 33⁰ BT ini berbatasan dengan Namibia, Botswana dan Zimbabwe di sebelah utara serta berbatasan dengan Mozambik dan Swaziland di sebelah timur laut. Di sebelah timur dan selatan merupakan samudera Hindia sedangkan di sebelah baratnya adalah samudera Atlantik. Di dalam negara Afrika Selatan terdapat sebuah negara berdaulat yang terkurung daratan (tidak memiliki laut) yaitu negara Lesotho.</p>

            <p>Afrika Selatan mempunyai iklim yang berbeda-beda. Di barat daya negara ini, iklimnya adalah Mediterania, di kawasan pendalaman ia beriklim sederhana, dan di timur laut iklimnya adalah subtropis. Afrika Selatan merupakan sebuah negara yang kaya dengan bahan tambang bernilai seperti emas, platinum dan berlian. Bahan tambang semula jadinya termasuk emas, kromium, antimoni, arang, biji besi, manganese, nikel, fosfat, biji timah, uranium, berlian, platinum, kuprum, vanadium, garam, gas asli. </p>

